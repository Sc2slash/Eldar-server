package server.utilities;

public class Vector {
	public static class Vec2f{
		public Vec2f(float x, float y){
			this.x = x;
			this.y = y;
		}
		public float x, y;
	}
	public static class Vec2i{
		public Vec2i(int x, int y){
			this.x = x;
			this.y = y;
		}
		public int x, y;
	}
}
